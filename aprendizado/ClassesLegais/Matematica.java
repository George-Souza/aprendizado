public class Matematica {
    public static void main(String[] args) {
        //Math.max(a, b) retorna o maior valor entre a e b
        System.out.println(Math.max(10, 15));
        //Math.min(a, b) retorna o menor valor entre a e b
        System.out.println(Math.min(5, 8));
        //Math.sqrt() retorna a raíz quadrada de um número
        System.out.println(Math.sqrt(64));
        //Math.abs() retorna o valor absoluto de um número
        System.out.println(Math.abs(-45));
        //Math.random() retorna um num aleatório entre 0 e 1
        System.out.println(Math.random());
        //pode multiplicar por um número para ter um range maior
        System.out.println((int)(Math.random()*11));
        //cbrt() retorna a raíz cúbica de um número
        System.out.println(Math.cbrt(27));
        //ceil() retorna um um número arredondado pra cima
        System.out.println(Math.ceil(7.4));
        //floor() retorna um número arredondado pra baixo
        System.out.println(Math.floor(7.6));
        //pow(a, b) retorna o valor a elevado à b
        System.out.println(Math.pow(2, 5));
        
    }
}
